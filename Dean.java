public class Dean {
   private String name;
    private int age;
    private String qualification;

    public Dean(String name, int age, String qualification) {
      this.name = name;
        this.age = age;
        this.qualification = qualification;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
   }

    public String getQualification() {
        return qualification;
    }
}
