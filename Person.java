class Person{
    private String name;
    private int age;
    private String gender;

 public void setName(String personName) 
{
     name=personName;
}
   public void setAge(int personAge)
 {
       age=personAge;
  }
    public void setGender(String personGender) 
{
        gender=personGender;
}
public String getName() 
{
        return name;
}
public int getAge()
{
	return age;
 }
 public String getGender()
 {
        return gender;
    }
}
